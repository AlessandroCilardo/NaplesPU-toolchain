#ifndef LLVM_LIB_TARGET_NAPLESPU_INSTPRINTER_NAPLESPUINSTPRINTER_H
#define LLVM_LIB_TARGET_NAPLESPU_INSTPRINTER_NAPLESPUINSTPRINTER_H

#include "llvm/MC/MCInstPrinter.h"
#include "llvm/Support/Compiler.h"

namespace llvm {

class NaplesPUInstPrinter : public MCInstPrinter {
public:
  NaplesPUInstPrinter(const MCAsmInfo &MAI, const MCInstrInfo &MII,
                   const MCRegisterInfo &MRI)
      : MCInstPrinter(MAI, MII, MRI) {}

  void printRegName(raw_ostream &OS, unsigned RegNo) const override;
  void printInst(const MCInst *MI, raw_ostream &O, StringRef Annot,
                 const MCSubtargetInfo &STI) override;

  // Autogenerated by tblgen.
  void printInstruction(const MCInst *MI, raw_ostream &O);
  static const char *getRegisterName(unsigned RegNo);

  void printOperand(const MCInst *MI, int opNum, raw_ostream &OS);
  void printMemOperand(const MCInst *MI, int opNum, raw_ostream &OS);
};
} // end namespace llvm

#endif
